#include "minirt.h"

t_vec2	texture_sp(t_vec3 *p, t_sp *sp)
{
	t_vec2	t;

	t.x = (1.0f + atan2(p->z, p->x) / M_PI) * 0.5f;
	t.y = acosf(p->y) / M_PI;
	return (t);
}

